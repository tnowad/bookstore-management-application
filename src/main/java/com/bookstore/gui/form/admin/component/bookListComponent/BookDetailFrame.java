    public void setStatus(Enum status) {
        int index = -1;
        switch (status.toString()) {
            case "AVAILABLE" -> {
                index = 0;
            }
            case "UNAVAILABLE" -> {
                index = 1;
            }
            case "DELETED" -> {
                index = 2;
            }
        }
        getStatus.setSelectedIndex(index);

    }

